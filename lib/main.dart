  void dispose() {
    _emailController.dispose();
    _passwordController.dispose();
    super.dispose();
  }

  void _login() {
    if (_formKey.currentState!.validate()) {
      // Aqui você pode adicionar a lógica de autenticação
      String email = _emailController.text;
